    // Gets all parts in the data base and displays relevent information
    // about them on the table.
    private void populateStock() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Franchisee franchisee = Franchisee.getFranchisee();
        List<Part> parts = franchisee.getStock();

        for (Part part : parts) {
            model.insertRow(
                    model.getRowCount(), new Object[]{
                part.getId(),
                part.getPartName(),
                part.getPartCode(),
                part.getManufacturer(),
                part.getVehicleType(),
                "£ " + part.getUnitPrice(),
                part.getLowLevelThreshold(),
                part.getStockLevel()
            }
            );
        }

    }

