    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SessionFactory sessionFactory = DBConnectivity.getSessionFactory();
        Session session = sessionFactory.getCurrentSession();
        
        try{
            session.beginTransaction();
            
            CustomerAccount ca = session.get(CustomerAccount.class, id);
            DiscountPlan ds = ca.getDiscountPlan();
            FlexibleBands f = new FlexibleBands((float) Integer.parseInt(min.getText())
                    , (float) Integer.parseInt(max.getText())
                    , (float) Integer.parseInt(value.getText()));
            
            ds.getFlexibleDiscount().add(f);
            f.setDiscountPlan(ds);
            
            session.save(f);
            session.update(ds);
            session.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Band added successfully");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Invalid data");
        }finally{
            session.close();
            garits.backButton(this);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

